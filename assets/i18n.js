// Apply language to the page
(function() {
  const LANGS = ['zh', 'en', 'ja'];
  const LANG_LABEL = { zh: '中', en: 'EN', ja: '日' };

  function getLang() {
    return localStorage.getItem('kong-lang') || 'zh';
  }
  function setLang(l) {
    localStorage.setItem('kong-lang', l);
    document.documentElement.lang = l === 'zh' ? 'zh-CN' : (l === 'ja' ? 'ja' : 'en');
    apply(l);
    syncSwitcher();
    window.dispatchEvent(new CustomEvent('langchange', { detail: l }));
  }
  function t(key, lang) {
    const d = window.I18N[lang] || window.I18N.en;
    if (key in d) return d[key];
    if (key in window.I18N.en) return window.I18N.en[key];
    return null;
  }
  function apply(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const v = t(key, lang);
      if (v == null) return;
      // Keys ending in .html allow raw HTML; others escape and convert \n to <br>.
      if (key.endsWith('.html')) {
        el.innerHTML = v;
      } else if (v.includes('\n')) {
        el.innerHTML = v.split('\n').map(escapeHtml).join('<br>');
      } else {
        el.textContent = v;
      }
    });
    document.querySelectorAll('[data-i18n-attr]').forEach(el => {
      const spec = el.getAttribute('data-i18n-attr'); // "attr:key,attr:key"
      spec.split(',').forEach(pair => {
        const [attr, key] = pair.split(':').map(s => s.trim());
        const v = t(key, lang);
        if (v != null) el.setAttribute(attr, v);
      });
    });
  }
  function escapeHtml(s) {
    return s.replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
  }

  function buildSwitcher() {
    const host = document.querySelector('[data-langswitch]');
    if (!host) return;
    host.innerHTML = LANGS.map(l =>
      `<button class="lang-btn" data-lang="${l}">${LANG_LABEL[l]}</button>`
    ).join('');
    host.querySelectorAll('button').forEach(b => {
      b.addEventListener('click', () => setLang(b.dataset.lang));
    });
  }
  function syncSwitcher() {
    const cur = getLang();
    document.querySelectorAll('[data-langswitch] button').forEach(b => {
      b.classList.toggle('on', b.dataset.lang === cur);
    });
  }

  function init() {
    buildSwitcher();
    apply(getLang());
    syncSwitcher();
    document.documentElement.lang = getLang() === 'zh' ? 'zh-CN' : (getLang() === 'ja' ? 'ja' : 'en');
    document.documentElement.classList.add('i18n-ready');
  }

  // Set <html lang> immediately so CSS / browser see the right language ASAP
  try {
    const _l = getLang();
    document.documentElement.lang = _l === 'zh' ? 'zh-CN' : (_l === 'ja' ? 'ja' : 'en');
  } catch (e) {}

  // Apply translations as soon as <body> exists, then re-run on DOMContentLoaded
  // to catch any nodes that hadn't been parsed yet.
  function earlyApply() {
    if (!document.body) return false;
    apply(getLang());
    return true;
  }

  if (document.readyState === 'loading') {
    // Poll for body existence so we translate above-the-fold content as it's parsed
    const _poll = setInterval(() => {
      if (earlyApply()) clearInterval(_poll);
    }, 16);
    document.addEventListener('DOMContentLoaded', () => {
      clearInterval(_poll);
      init();
    });
  } else {
    init();
  }

  window.Kong_i18n = { getLang, setLang, t };
})();
